public class PrintOrder {
    private String isbn;
    private int count;
    private long printingHouseId;
    private boolean isDone;

    public String getIsbn() {
        return isbn;
    }

    public int getCount() {
        return count;
    }

    public long getPrintingHouseId() {
        return printingHouseId;
    }

    public boolean isDone() {
        return isDone;
    }
}
